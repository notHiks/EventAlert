function EventAlert_LoadAlerts_DemonHunter()

-- Custom
	if EA_CustomItems[EA_CLASS_DEMON_HUNTER] == nil then EA_CustomItems[EA_CLASS_DEMON_HUNTER] = {} end;

-- Normal
	if EA_Items[EA_CLASS_DEMON_HUNTER] == nil then EA_Items[EA_CLASS_DEMON_HUNTER] = {} end;

-- Alternate
	if EA_AltItems[EA_CLASS_DEMON_HUNTER] == nil then EA_AltItems[EA_CLASS_DEMON_HUNTER] = {} end;

--Stacking
	if EA_StackingItems[EA_CLASS_DEMON_HUNTER] == nil then EA_StackingItems[EA_CLASS_DEMON_HUNTER] = {} end;
	if EA_StackingItemsCounts[EA_CLASS_DEMON_HUNTER] == nil then EA_StackingItemsCounts[EA_CLASS_DEMON_HUNTER] = {} end;

--All credit goes to the original author, CurtisTheGreat
end
